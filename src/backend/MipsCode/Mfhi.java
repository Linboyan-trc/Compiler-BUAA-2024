package backend.MipsCode;

import backend.ValueMeta.Reg;

public class Mfhi implements MipsCode {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // 1. mfhi指令
    private final Reg reg;

    public Mfhi(Reg reg) {
        this.reg = reg;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // 1. 生成mips代码
    @Override
    public String toString() {
        return "mfhi " + reg;
    }
}
